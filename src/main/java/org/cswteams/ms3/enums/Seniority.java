package org.cswteams.ms3.enums;

/**
 * This enumeration provides all the possible values for specification of a <i>Doctor</i>'s seniority.
 *
 * @see org.cswteams.ms3.entity.Doctor
 */
public enum Seniority {
    /**
     * Structured doctor.
     */
    STRUCTURED,

    /**
     * First two years-specialist.
     */
    SPECIALIST_JUNIOR,

    /**
     * Specialist after second year
     */
    SPECIALIST_SENIOR
}